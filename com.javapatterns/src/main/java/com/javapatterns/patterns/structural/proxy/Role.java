package com.javapatterns.patterns.structural.proxy;

/**
 * 
 * POJO for role
 *
 */
public class Role {
	
	private String role;

	/**
	 * @return the role
	 */
	public String getRole() {
		return role;
	}

	/**
	 * @param role the role to set
	 */
	public void setRole(String role) {
		this.role = role;
	}
	

}
